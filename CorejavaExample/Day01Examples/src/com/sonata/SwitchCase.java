package com.sonata;

public class SwitchCase 
{
	public static void main (String args[])
	{
		int d=59;
		switch((d/10)*10)
		{
			case 50:
				System.out.println("false");
				break;
			case 60:
				System.out.println("true");
				break;
			case 70:
				System.out.println("false");
				break;	
			
		}
	}
}


